    private void btnPesquisarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarClienteActionPerformed

        int id;

        try {
            id = Integer.parseInt(txtIdCliente.getText());

        } catch (NumberFormatException e) {
            id = -1;
            JOptionPane.showMessageDialog(null, "Coloque um valor Id cliente adequado!", "Erro", JOptionPane.ERROR_MESSAGE);
        }

        if (id != -1) {

            int indiceLinha = -1;
            for (int i = 0; i < tblLocacoes.getRowCount(); i++) {
                if (tblLocacoes.getValueAt(i, 4).equals(id)) {
                    indiceLinha = i;
                    break;
                }
            }

            if (indiceLinha != -1) {
                tblLocacoes.setRowSelectionInterval(indiceLinha, indiceLinha);
            } else {
                JOptionPane.showMessageDialog(null, "Id cliente não encontrado na tabela!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnPesquisarClienteActionPerformed

