    private Connection conectarBanco() throws SQLException {
        String url = "jdbc:mysql://localhost:3306/locadoraDeCarros";
        String usuario = "root";
        String senha = "root";
        return DriverManager.getConnection(url, usuario, senha);
    }

    private void exibirRegistros() throws SQLException {
        try {
            Connection conexao = conectarBanco();

            String sql = "SELECT data_inicio, data_fim, statusCarro, carro_id, cliente_id FROM Locacao";

            // Data inicio, data final, Status, Id carro, Id cliente
            PreparedStatement pstmt = conexao.prepareStatement(sql);

            ResultSet rs = pstmt.executeQuery();

            DefaultTableModel model = (DefaultTableModel) tblLocacoes.getModel();
            model.setRowCount(0);

            while (rs.next()) {

                Date dataInicio = rs.getDate("data_inicio");
                Date dataFim = rs.getDate("data_fim");

                SimpleDateFormat formato = new SimpleDateFormat("dd-MM-yyyy");

                String dataInicioFormatada = formato.format(dataInicio);
                String dataFimFormatada = formato.format(dataFim);

                String status = rs.getString("statusCarro");
                int idCarro = rs.getInt("carro_id");
                int idCliente = rs.getInt("cliente_id");

                model.addRow(new Object[]{dataInicioFormatada, dataFimFormatada, status, idCarro, idCliente});

            }

            rs.close();
            pstmt.close();
        } catch (SQLException ex) {
            // Lidar com erros de banco de dados
            JOptionPane.showMessageDialog(this, "Erro ao buscar registros: " + ex.getMessage());
        }

    }

